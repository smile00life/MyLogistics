package com.logistics.model;

public class WorkerInfo {

	private int worker_id;
	private String worker_name;
	private String worker_age;
	private String worker_sex;
	private String worker_code;
	private int role_id;
	private String worker_type;
	public int getWorker_id() {
		return worker_id;
	}
	public void setWorker_id(int worker_id) {
		this.worker_id = worker_id;
	}
	public String getWorker_name() {
		return worker_name;
	}
	public void setWorker_name(String worker_name) {
		this.worker_name = worker_name;
	}
	public String getWorker_age() {
		return worker_age;
	}
	public void setWorker_age(String worker_age) {
		this.worker_age = worker_age;
	}
	public String getWorker_sex() {
		return worker_sex;
	}
	public void setWorker_sex(String worker_sex) {
		this.worker_sex = worker_sex;
	}
	public String getWorker_code() {
		return worker_code;
	}
	public void setWorker_code(String worker_code) {
		this.worker_code = worker_code;
	}
	public int getRole_id() {
		return role_id;
	}
	public void setRole_id(int role_id) {
		this.role_id = role_id;
	}
	public String getWorker_type() {
		return worker_type;
	}
	public void setWorker_type(String worker_type) {
		this.worker_type = worker_type;
	}
	
}
